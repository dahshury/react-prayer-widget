/**
 * @deprecated Use getRandomContentFromTranslations() instead
 */
export function getRandomContent(): IslamicContent {
	const randomIndex = Math.floor(
		Math.random() * DEFAULT_ISLAMIC_CONTENT.length
	);
	return DEFAULT_ISLAMIC_CONTENT[randomIndex];
}

/**
 * @deprecated Use getContentByTypeFromTranslations() instead
 */
export function getContentByType(type: "hadith" | "ayah"): IslamicContent[] {
	return DEFAULT_ISLAMIC_CONTENT.filter((content) => content.type === type);
}

/**
 * Get random content from translations
 */
export async function getRandomContentFromTranslations(
	locale: "en" | "ar" = "en"
): Promise<IslamicContent> {
	const content = await getIslamicContentFromTranslations(locale);
	if (content.length === 0) {
		// Fallback to default
		return getRandomContent();
	}
	const randomIndex = Math.floor(Math.random() * content.length);
	return content[randomIndex];
}

/**
 * Get content by type from translations
 */
export async function getContentByTypeFromTranslations(
	type: "hadith" | "ayah",
	locale: "en" | "ar" = "en"
): Promise<IslamicContent[]> {
	const content = await getIslamicContentFromTranslations(locale);
	if (content.length === 0) {
		// Fallback to default
		return getContentByType(type);
	}
	return content.filter((item) => item.type === type);
}
